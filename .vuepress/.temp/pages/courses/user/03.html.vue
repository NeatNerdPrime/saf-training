<details class="hint-container details"><summary>3. What is HDF?</summary>
<p><a href="https://saf.mitre.org/#/normalize" target="_blank" rel="noopener noreferrer">HDF<ExternalLinkIcon/></a>, or Heimdall Data Format, is a common format to represent normalized security data. HDF files record vital security data about a completed validation test, such as the test code, description, attributes, and outcome. This allows for the aggregation and analysis of test results from a wide range of validation tools at once.</p>
<p>HDF data can be easily visualized in <a href="https://heimdall-lite.mitre.org/" target="_blank" rel="noopener noreferrer">Heimdall<ExternalLinkIcon/></a>, the SAF's visualization application.</p>
